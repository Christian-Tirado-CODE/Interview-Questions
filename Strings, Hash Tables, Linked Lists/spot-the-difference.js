
/* This question is asked by Google. You are given two strings, s and t 
which only consist of lowercase letters. t is generated by shuffling the letters 
in s as well as potentially adding an additional random character. 
Return the letter that was randomly added to t if it exists, otherwise, return ’ ‘.
Note: You may assume that at most one additional character can be added to t.

 */



function spotTheDiff(s, t){
    const hashMap = new Map();
    
    for(c of s){
      if(hashMap.has(c))
        hashMap.set(c, hashMap.get(c) + 1);
      else
        hashMap.set(c, 1);
    }
    
    for(c of t){
      if(!hashMap.has(c) || hashMap.get(c) === 0)
      return c;
      else
      hashMap.set(c, hashMap.get(c) - 1);
    } 
    
    
    
    return '';
  }
  
  // Time Complexity: O(N) where N is the size of both string s and t.
  // Space Complexity: O(N) where N is the characters stored in the 
  //hash table.
  
  console.log(spotTheDiff("foobar", "barfoot"));
  console.log(spotTheDiff("ide", "idea"));
  console.log(spotTheDiff("coding", "ingcod"));